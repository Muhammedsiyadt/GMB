function calculateDistance(lat1, lon1, lat2, lon2, gridSize) {
    const R = 6371e3; // metres
    const φ1 = lat1 * Math.PI / 180;
    const φ2 = lat2 * Math.PI / 180;
    const Δφ = (lat2 - lat1) * Math.PI / 180;
    const Δλ = (lon2 - lon1) * Math.PI / 180;

    const a = Math.sin(Δφ / 2) * Math.sin(Δφ / 2) +
              Math.cos(φ1) * Math.cos(φ2) *
              Math.sin(Δλ / 2) * Math.sin(Δλ / 2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

    const gridDistance = R * c;
    const gridSizeMeters = gridSize * 1000; // Convert grid size from kilometers to meters

    return gridDistance / gridSizeMeters; // Return distance divided by grid size to normalize it
}

module.exports = calculateDistance;
